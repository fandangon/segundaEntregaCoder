const guardarLocalmente = (clave, valor) => { localStorage.setItem(clave, valor) }

var productoEncontrado;

function agregarCarrito(id, id1) {

    switch (id) {
        case 1:
            productoEncontrado = productosCuidado.find(productos => productos.id === id1);
            productosCarritos.push(productoEncontrado);
            guardarLocalmente("productos", JSON.stringify(productosCarritos));

            break;
        case 2:

            productoEncontrado = productosSuplementos.find(productos => productos.id === id1);
            productosCarritos.push(productoEncontrado);
            guardarLocalmente("productos", JSON.stringify(productosCarritos));


            break;
        case 3:
            productoEncontrado = productosBebe.find(productos => productos.id === id1);
            productosCarritos.push(productoEncontrado);
            guardarLocalmente("productos", JSON.stringify(productosCarritos));

            break;
        case 4:
            productoEncontrado = productosDermo.find(productos => productos.id === id1);
            productosCarritos.push(productoEncontrado);
            guardarLocalmente("productos", JSON.stringify(productosCarritos));

            break;
        case 5:
            productoEncontrado = productosPerfumes.find(productos => productos.id === id1);
            productosCarritos.push(productoEncontrado);
            guardarLocalmente("productos", JSON.stringify(productosCarritos));
            break;
    }
}
